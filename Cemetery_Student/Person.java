import java.util.*;

public class Person {
    // instance variables - replace the example below with your own
    private int iq;
    private String name;

    public Person(String n, int i) {
        // initialise instance variables
        name = n;
        iq = i;
    }

    public int getIQ() {
        return iq;
    }

    public String getName() {
        return name;
    }
}
